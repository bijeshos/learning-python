# ---------------------------------------------------------
# assignment
x = 5  # int
y = 2.8  # float
z = 1j  # complex

# printing values
print("-----------------------")
print(x)
print(y)
print(z)

print("-----------------------")
print("x: " + str(x))
print("y: " + str(y))
print("z: " + str(z))

# printing types of variables
print("-----------------------")
print(type(x))
print(type(y))
print(type(z))

# ---------------------------------------------------------
# casting
i = int(1)
j = float(2.8)
k = str("3")

print("-----------------------")
print(i)
print(j)
print(k)

# ---------------------------------------------------------
# string and string operations
msg = "hello, john "

print("-----------------------")
print("msg: " + msg)
print(msg[1])  # char at a position
print(msg[1:3])  # substring
print(msg.capitalize())
print(msg.casefold())
print(msg.strip())
print(msg.upper())
print(len(msg))
print(msg.split(","))
print("-----------------------")

